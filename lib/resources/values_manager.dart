///
///This class is used as a margin, padding, ..etc constants holder
///
///For more Fine grained control you can separate each concern to
///its separated class for ex. AppMargin, AppPadding, ..etc.
///
///This will allow you later to control specific padding value for example
///without affecting the corresponding margin value, so you may hve p8 in
///padding that hold a different value from the m8 in the margin class
///
abstract class AppValues {
  static const v0 = 0.0;
  static const v0_8 = 0.8;
  static const v1_5 = 1.5;
  static const v4 = 4.0;
  static const v8 = 8.0;
  static const v10 = 10.0;
  static const v12 = 12.0;
  static const v14 = 14.0;
  static const v16 = 16.0;
  static const v18 = 18.0;
  static const v20 = 20.0;
  static const v30 = 30.0;
  static const v40 = 40.0;
  static const v50 = 50.0;
  static const v60 = 60.0;
  static const v100 = 100.0;
}
